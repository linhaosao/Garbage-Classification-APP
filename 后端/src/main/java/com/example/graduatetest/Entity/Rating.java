package com.example.graduatetest.Entity;

public class Rating {
    private String Rating_id;
    private String Rating_phonenumer;
    private String Rating_bar;
    private String Rating_date;

    public String getRating_id() {
        return Rating_id;
    }

    public void setRating_id(String rating_id) {
        Rating_id = rating_id;
    }

    public String getRating_phonenumer() {
        return Rating_phonenumer;
    }

    public void setRating_phonenumer(String rating_phonenumer) {
        Rating_phonenumer = rating_phonenumer;
    }

    public String getRating_bar() {
        return Rating_bar;
    }

    public void setRating_bar(String rating_bar) {
        Rating_bar = rating_bar;
    }

    public String getRating_date() {
        return Rating_date;
    }

    public void setRating_date(String rating_date) {
        Rating_date = rating_date;
    }
}
