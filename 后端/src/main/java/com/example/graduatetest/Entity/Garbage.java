package com.example.graduatetest.Entity;

public class Garbage {
    private String garbage_id;
    private String garbage_name;
    private String garbage_cat;
    private String garbage_other;


    public String getGarbage_id() {
        return garbage_id;
    }

    public void setGarbage_id(String garbage_id) {
        this.garbage_id = garbage_id;
    }

    public String getGarbage_name() {
        return garbage_name;
    }

    public void setGarbage_name(String garbage_name) {
        this.garbage_name = garbage_name;
    }

    public String getGarbage_cat() {
        return garbage_cat;
    }

    public void setGarbage_cat(String garbage_cat) {
        this.garbage_cat = garbage_cat;
    }

    public String getGarbage_other() {
        return garbage_other;
    }

    public void setGarbage_other(String garbage_other) {
        this.garbage_other = garbage_other;
    }
}
